function buildMovingPrepResult(payload) {
  const combined = Object.values(payload).join(' ');
  const hasBooks = /本|書籍|漫画|雑誌/.test(combined);
  const hasElectronics = /PC|パソコン|モニター|iMac|家電|掃除機|炊飯器|スピーカー/.test(combined);
  const hasHeavy = /ダンベル|ベンチ|重い|大型|机|デスク/.test(combined);

  return {
    summary: '入力内容をもとに、引越し準備のたたき台を作成しました。',
    boxEstimate: hasBooks
      ? '本や小物があるため、小さめの箱を多めに分ける想定で準備してください。'
      : '衣類・小物の量に合わせて、まずは少なめに箱数を見積もり、足りない分を追加する想定で準備してください。',
    materials: [
      'ダンボール箱',
      'ガムテープ、油性ペン、緩衝材',
      '当日バッグ',
      hasElectronics ? '電子機器用の緩衝材・ケーブル整理袋' : '小物をまとめる袋やケース'
    ],
    checklist: [
      '荷物を、家具・衣類・家電・本/小物・貴重品に分けて確認する',
      'ライフライン、郵便転送、住所変更、ネット回線の手続きを確認する',
      '前日までに当日バッグと掃除道具を分けておく',
      '見積もり依頼時に、重い物・壊れやすい物・大型家具を伝えられるようにする'
    ],
    risks: [
      hasHeavy ? '重い荷物は箱に詰めすぎず、小分けにしてください。' : '箱が重くなりすぎないよう、衣類・本・小物を分けてください。',
      hasElectronics ? 'PCやモニターなどはケーブルを外す前に写真を撮ると復旧しやすいです。' : '壊れやすい物は緩衝材で包み、箱の外側に分かるように書いてください。',
      'この結果は正式見積もりではありません。実際の条件は引越し業者や管理会社に確認してください。'
    ],
    disclaimer: 'この結果は目安です。正式な見積もり、契約判断、専門的助言の代替ではありません。'
  };
}
